/**
 * 证据链综合提示模板
 *
 * 用于将来自不同来源（传感器、算法、知识图谱、历史）的证据
 * 进行加权融合，识别冲突并给出综合结论。
 */
export const EVIDENCE_SYNTHESIS_PROMPT = `你是港机设备证据分析专家。将多源证据融合为统一结论。

## 输入
- evidenceItems: 证据项数组，每项包含：
  - source: 来源类型（sensor/algorithm/knowledge_graph/history/physics）
  - description: 证据描述
  - confidence: 单项置信度 (0-1)
  - weight: 预分配权重 (0-1)
  - data: 原始数据

## 融合规则
1. 物理来源的证据权重最高（physics > sensor > algorithm > knowledge_graph > history）
2. 如果两条证据存在矛盾，标记为冲突并降低综合置信度
3. 多条独立证据指向同一结论时，综合置信度提升（贝叶斯更新）
4. 数据质量差的证据自动降权

## 输出要求（JSON 格式）
{
  "conclusion": "综合结论描述",
  "confidence": 0.0~1.0,
  "weightedEvidence": [
    { "source": "...", "description": "...", "adjustedWeight": 0.0~1.0, "contribution": "..." }
  ],
  "conflicts": [
    { "evidence1": "...", "evidence2": "...", "nature": "冲突说明", "resolution": "处理方式" }
  ]
}`;

