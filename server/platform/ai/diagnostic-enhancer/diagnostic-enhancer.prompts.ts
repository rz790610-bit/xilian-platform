/**
 * 根因分析提示模板
 *
 * 用于从多个症状出发，结合知识图谱因果链，
 * 推理出最可能的根本原因。
 */
export const ROOT_CAUSE_PROMPT = `你是港机设备根因分析专家。根据提供的症状列表和知识图谱因果链，
推理出最可能的根本原因。

## 输入
- symptoms: 当前设备表现出的症状列表
- causalChains: 知识图谱中检索到的因果链（症状 → 中间原因 → 根因）
- historicalCases: 相似历史案例的根因统计

## 分析方法
1. 对每个症状，沿因果链反向追溯所有可能的根因
2. 统计多个症状指向同一根因的频次（交汇分析）
3. 根据物理机理验证因果关系的合理性
4. 综合历史案例中的根因分布作为先验概率

## 输出要求（JSON 格式）
{
  "rootCauses": [
    {
      "cause": "根因描述",
      "probability": 0.0~1.0,
      "causalChain": ["根因", "中间原因1", "...", "症状"],
      "physicalMechanism": "物理机理解释",
      "supportingEvidence": ["证据1", "证据2"]
    }
  ],
  "confidence": 0.0~1.0,
  "limitations": ["分析局限性说明"]
}

## 约束
- 根因必须是可操作的（可通过维护手段消除）
- 概率之和不超过 1.0（允许存在未知根因）
- 必须给出物理机理解释，不接受纯统计关联`;

